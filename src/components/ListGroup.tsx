function ListGroup() {
  const items = ["Chicago", "Berlin", "Montreal", "Vancouver", "Barcelona"];

  return (
    <>
      <h1>List</h1>
      <ul className="list-group">
        {items.map((item) => (
          <li key={item}>{item}</li>
        ))}
      </ul>
    </>
  );
}

export default ListGroup;
